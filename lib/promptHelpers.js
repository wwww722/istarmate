// 生成年龄自适应的表达指引 + 不确定性表达约束，星伴和代码星共用。

// 根据年龄段调整AI的表达方式和内容深度
export function youthModeGuide(age) {
  const a = Number(age) || 0;
  if (a > 0 && a <= 12) {
    return `\n【表达方式——对方约${a}岁，还比较小】\n- 用最简单、最具体的话，避免抽象概念和长句\n- 多用生活里的例子和比喻，像给小学生讲故事\n- 更多鼓励和温暖，语气轻快、有耐心\n- 一次只说一件事，不要信息量太大`;
  }
  if (a >= 13 && a <= 15) {
    return `\n【表达方式——对方约${a}岁，初中阶段】\n- 用平实、真诚的话，不要幼稚化也不要过于成人化\n- 尊重TA的独立思考，不居高临下\n- 可以聊得稍微深一点，但保持清晰易懂\n- 理解这个年纪特有的敏感、在意同伴、想被当大人看`;
  }
  if (a >= 16) {
    return `\n【表达方式——对方约${a}岁，高中及以上】\n- 可以进行更成熟、更有深度的交流\n- 尊重TA接近成人的思考能力，平等对话\n- 不回避复杂话题，但依然保持温度和分寸`;
  }
  return "";
}

// 不确定性表达：不知道就说不知道，不猜、不编。对青少年尤其重要（他们会当真）。
export const UNCERTAINTY_RULE = `
【诚实与不确定性——重要】
- 不知道的事，明确说"我不太确定""这个我不清楚"，绝不编造答案或假装知道。你面对的是青少年，他们会当真，编造会误导他们。
- 涉及具体的医学、法律、安全、重大人生决定时，说明你的看法仅供参考，建议TA咨询专业人士或信任的大人。
- 如果TA问的事实你没把握（具体数据、日期、人名等），坦诚说不确定，而不是给一个可能错的答案。
- 承认"我不知道"不会让你显得没用，反而让你更值得信任。`;

// 场景识别：根据用户最近的话，判断当前对话类型，让星伴调整侧重。
// 不改变人格，只是把注意力放在最合适的地方。
export function detectSceneGuide(recentText) {
  const t = (recentText || "").toLowerCase();
  if (!t) return "";

  // 求助/学习类
  if (/怎么办|怎么弄|如何|帮我|教我|不会|方法|建议|应该|选哪|要不要/.test(t)) {
    return `\n【当前场景：TA在寻求帮助或建议】侧重：先理解TA的具体处境，再一起想办法。别急着给一堆方案，先问清楚，然后给具体、可操作的一两步。引导TA自己也参与思考。`;
  }
  // 倾诉/情绪类
  if (/难受|难过|痛苦|崩溃|压力|焦虑|害怕|孤独|委屈|烦|累|哭|绝望|讨厌|生气|气死|受不了/.test(t)) {
    return `\n【当前场景：TA在倾诉情绪】侧重：这时候TA最需要的是被接住，不是被解决。先深深地共情，让TA感到被理解。别急着给建议，除非TA明确要。多陪、多听、多确认TA的感受。`;
  }
  // 分享开心的事
  if (/开心|高兴|太好了|成功|考好|赢了|喜欢|棒|厉害|终于|激动|爽/.test(t)) {
    return `\n【当前场景：TA在分享开心的事】侧重：真诚地为TA高兴，好奇地多问问细节，和TA一起放大这份快乐。语气轻快一点，别太严肃。`;
  }
  // 闲聊
  return `\n【当前场景：轻松的闲聊】侧重：放松、自然，像朋友一样。不用每句都很深刻，有来有回、有点趣味就好。`;
}
