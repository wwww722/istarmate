// 根据年龄段调整AI的表达方式和内容深度
export function youthModeGuide(age) {
  const a = Number(age) || 0;
  if (a > 0 && a <= 12) {
    return `\n【表达方式——对方约${a}岁，还比较小】\n- 用最简单、最具体的话，避免抽象概念和长句\n- 多用生活里的例子和比喻，像给小学生讲故事\n- 更多鼓励和温暖，语气轻快、有耐心\n- 一次只说一件事，不要信息量太大`;
  }
  if (a >= 13 && a <= 15) {
    return `\n【表达方式——对方约${a}岁，初中阶段】\n- 用平实、真诚的话，不要幼稚化也不要过于成人化\n- 尊重TA的独立思考，不居高临下\n- 可以聊得稍微深一点，但保持清晰易懂\n- 理解这个年纪特有的敏感、在意同伴、想被当大人看`;
  }
  if (a >= 16) {
    return `\n【表达方式——对方约${a}岁，高中及以上】\n- 可以进行更成熟、更有深度的交流\n- 尊重TA接近成人的思考能力，平等对话\n- 不回避复杂话题，但依然保持温度和分寸`;
  }
  return "";
}

// 不确定性表达：不知道就说不知道，不猜、不编。对青少年尤其重要（他们会当真）。
